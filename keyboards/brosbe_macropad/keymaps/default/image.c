static char screen_image[512];

void render_icon(const char icon[], int screen_index, bool addRing){
    if(addRing)
    {
        for(int i = 0; i < 128; i++)
        {
            screen_image[screen_index + i] = icon[i] | ring[i];
        }
        return;
    }

    for(int i = 0; i < 128; i++)
    {
        screen_image[screen_index + i] = icon[i];
    }
}

void show_oled(int layer, int above, int below){

    render_icon(icons[above], 0, false);
    render_icon(icons[layer], 128, true);
    render_icon(icons[below], 256, false);
    render_icon(saddam, 384, false);
    oled_write_raw(screen_image, 512);
}
